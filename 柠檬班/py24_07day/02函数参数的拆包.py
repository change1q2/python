def func(a, b, c):
    print("a:", a)
    print("b:", b)
    print("c", c)


#调用函数的时候，传递参数的时候可以使用*对列表和元祖进行拆包
li = [11, 22, 33]
func(*li)

print("...........................")
#调用函数的时候，传递参数的时候可以使用**对字典进行拆包
dic = {"a": 111, "b": 222, "c": 444}
func(**dic)

